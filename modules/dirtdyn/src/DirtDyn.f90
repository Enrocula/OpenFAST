   real(DbKi),                        intent(inout)  :: Interval    !< Coupling interval in seconds: the rate that
                                                                     !!   (1) DirtDyn_UpdateStates() is called in loose coupling &
                                                                     !!   (2) DirtDyn_UpdateDiscState() is called in tight coupling.
